function hide(){
  var x = document.getElementById("thinkcode");
  var y = document.getElementById("pro");

  x.style.display = "none";
  y.style.display = "none";
}

function show(){
  var x = document.getElementById("thinkcode");
  var y = document.getElementById("pro");

  x.style.display = "block";
  y.style.display = "block";
}